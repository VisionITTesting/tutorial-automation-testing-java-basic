package com.vit._1_MyFirstCalcProgram;

public class Calculator {
    public int add(int i, int i1) {
        return i + i1;
    }

    public int add(int i, int i1, int i2) {
        return i + i1 + i2;
    }

    public int sub(int i, int i1) {
        return i-i1;
    }

    public int mul(int i, int i1) {
        return i*i1;
    }
}
